# 基于**kwargs的解包
def demo_dict(name, age):
    print(name)
    print(age)

data = {
    'name': 'asd',
    'age': 18
}

demo_dict(**data)

print("-------")

def demo_dict2(a):
    print(a)
    print(**a)

data = {
    'name': 'asd',
    'age': 18
}

demo_dict2(data)    # 会报错